export function cloneMinaCategories(value = MINA_DEFAULT_CATEGORIES) {
  return JSON.parse(JSON.stringify(value));
}

export function flattenMinaCategories(nodes = [], parentPath = []) {
  let result = [];

  nodes.forEach(node => {
    const path = [...parentPath, node.name];

    result.push({
      ...node,
      path,
      fullName: path.join("/")
    });

    if (Array.isArray(node.children) && node.children.length) {
      result = result.concat(flattenMinaCategories(node.children, path));
    }
  });

  return result;
}
